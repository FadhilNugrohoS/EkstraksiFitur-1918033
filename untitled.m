function pushbutton2_Callback(hObject, eventdata, handles)
open = guidata(gcbo);
data=get(open.axes1,'Userdata');
R=data(:,:,1);
G=data(:,:,2);
B=data(:,:,3);
red=mean(mean(R));
green=mean(mean(G));
blue=mean(mean(B));
rataRed=red/255;
